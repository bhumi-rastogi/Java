
public class Decimal {

	public static void main(String[] args) {
		double temp = 10.2;
		System.out.println(temp);
		
		// Integer default value is int
		// Decimal default value is double
		
		// Integer
		long te = 10^10L;
		System.out.println(te);
		
		// Decimal
		float f = 2.4f;
		System.out.println(f);

	}

}
